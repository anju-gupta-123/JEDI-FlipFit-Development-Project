package com.flipkart.bean;

public class Gym_Center {
	private int center_id;
	private int owner_id;
	private String center_name;
	private String address;
	private int no_of_slots;
	/**
	 * @return the center_id
	 */
	public int getCenter_id() {
		return center_id;
	}
	/**
	 * @param center_id the center_id to set
	 */
	public void setCenter_id(int center_id) {
		this.center_id = center_id;
	}
	/**
	 * @return the owner_id
	 */
	public int getOwner_id() {
		return owner_id;
	}
	/**
	 * @param owner_id the owner_id to set
	 */
	public void setOwner_id(int owner_id) {
		this.owner_id = owner_id;
	}
	/**
	 * @return the center_name
	 */
	public String getCenter_name() {
		return center_name;
	}
	/**
	 * @param center_name the center_name to set
	 */
	public void setCenter_name(String center_name) {
		this.center_name = center_name;
	}
	/**
	 * @return the address
	 */
	public String getAddress() {
		return address;
	}
	/**
	 * @param address the address to set
	 */
	public void setAddress(String address) {
		this.address = address;
	}
	/**
	 * @return the no_of_slots
	 */
	public int getNo_of_slots() {
		return no_of_slots;
	}
	/**
	 * @param no_of_slots the no_of_slots to set
	 */
	public void setNo_of_slots(int no_of_slots) {
		this.no_of_slots = no_of_slots;
	}
	

}
